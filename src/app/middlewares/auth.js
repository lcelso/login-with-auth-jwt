import jwt from 'jsonwebtoken';
import { promisify } from 'util';

import authConfig from '../../config/auth';

export default async (req, res, next) => {
  const authHeader = req.headers.authorization;

  if (!authHeader) {
    return res.status(401).json({ error: 'Token not provided' });
  }

  const [, token] = authHeader.split(' ');

  // jwt.verify utiliza a maneira de callback antiga
  // com promisify eu posso usar uma promise
  // por isto a chamada promisify(jwt.verify) que retorna uma funcao
  // no qual eu passo na segunda chamada (token, authConfig.secret)
  try {
    const decoded = await promisify(jwt.verify)(token, authConfig.secret);

    req.userId = decoded.id;

    return next();
  } catch (err) {
    return res.status(401).json({ error: 'Token invalid' });
  }
};
